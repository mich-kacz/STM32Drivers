#ifndef REGISTERS_H
#define REGISTERS_H


// Address for AHB1 clock
#define GPIO_AHB1_BUS 0x40020000

// Offsets for each gpio bus
#define GPIO_GPIOA (GPIO_AHB1_BUS + 0x00000000)
#define GPIO_GPIOB (GPIO_AHB1_BUS + 0x00000400)
#define GPIO_GPIOC (GPIO_AHB1_BUS + 0x00000800)
#define GPIO_GPIOD (GPIO_AHB1_BUS + 0x00000C00)
#define GPIO_GPIOE (GPIO_AHB1_BUS + 0x00001000)
#define GPIO_GPIOF (GPIO_AHB1_BUS + 0x00001400)
#define GPIO_GPIOG (GPIO_AHB1_BUS + 0x00001800)
#define GPIO_GPIOH (GPIO_AHB1_BUS + 0x00001C00)

// OFFSETS FOR EACH GPIO CONFIG
#define GPIO_MODER_OFFSET    0x00
#define GPIO_OTYPER_OFFSET   0x04
#define GPIO_OSPEEDER_OFFSET 0x08
#define GPIO_PUPDR_OFFSET    0x0C
#define GPIO_IDR_OFFSET      0x10
#define GPIO_ODR_OFFSET      0x14
#define GPIO_BSRR_OFFSET     0x18
#define GPIO_LCKR_OFFSET     0x1C
#define GPIO_AFRL_OFFSET     0x20
#define GPIO_AFRH_OFFSET     0x24


#endif